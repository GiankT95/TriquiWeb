    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        
        boolean flatError = false;
        for (int i = 1; i <= 9; i++) {            
            if (request.getParameter("" + i).equalsIgnoreCase("X") || request.getParameter("" + i).equalsIgnoreCase("O"))
                try {
                    Triqui.instance().asignarJugada(i, request.getParameter("" + i).equalsIgnoreCase("X")?1:2);
                    Triqui.instance().juegaMaquina();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
            else if(!request.getParameter("" + i).isEmpty())
                flatError = true;
        }
        
        if (flatError){
            request.setAttribute("Error", "Solo debe colocar X y O");
        }
            
        /*if(Triqui.instance().validar()==true){
           request.setAttribute("Error2", "Solo puede hacer una jugada");
       }*/
       
        
        request.setAttribute("Triqui", Triqui.instance().getTablero());
        request.getRequestDispatcher("index.jsp").forward(request, response);
    }

